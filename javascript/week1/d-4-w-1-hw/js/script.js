// Пайдаланушыдан үш санды енгізуін сұрау
let firstNumber = parseFloat(prompt("Бірінші санды енгізіңіз:"));
let secondNumber = parseFloat(prompt("Екінші санды енгізіңіз:"));
let thirdNumber = parseFloat(prompt("Үшінші санды енгізіңіз:"));

// Орташа арифметикасын формула арқылы есептеу
let average = (firstNumber + secondNumber + thirdNumber) / 3;

// Нәтижені alert функциясы арқылы көрсетуyy
alert("Енгізілген сандардың орташа арифметикалық мәні: " + average);
